class TestInstallDefusedXML(unittest.TestCase):

    def setUp(self):
        super(TestInstallDefusedXML, self).setUp()
        uninstall_defusedxml()

    def tearDown(self):
        super(TestInstallDefusedXML, self).tearDown()
        uninstall_defusedxml()

    def test_xmlrpc_lib_patched_xml_bomb_raises_entities_forbidden_error(self):
        install_defusedxml()

        with self.assertRaises(defusedxml.common.EntitiesForbidden):
            xmlrpclib.loads(XML_BOMB_PAYLOAD)
